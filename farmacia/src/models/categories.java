package models;

/**
 *
 * @author PtIngHome
 */
public class categories {
    //variables
    private int id;
    private String name;
    private String create;
    private String update;

    public categories() {
    }

    public categories(int id, String name, String create, String update) {
        this.id = id;
        this.name = name;
        this.create = create;
        this.update = update;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCreate() {
        return create;
    }

    public void setCreate(String create) {
        this.create = create;
    }

    public String getUpdate() {
        return update;
    }

    public void setUpdate(String update) {
        this.update = update;
    }
    
    
    
    
    
}
